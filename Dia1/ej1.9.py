numero = int(input('Introduzca un entero: '))
print(f'El número introducido fue: {numero}')

numero = numero + 7
print(f'El número más 7 es: {numero}')

numero = numero * 2
print(f'El resultado multiplicado por 2 es: {numero}')

numero = numero - 4
print(f'El resultado menos 4 es: {numero}')

numero = numero // 3
print(f'El resultado de la división entera entre 3 es: {numero}')